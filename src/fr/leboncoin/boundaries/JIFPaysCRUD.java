    private void remplirJTable() {
        try {

            idtm = (DefaultTableModel) jTablePays.getModel();

            Object[] tLigne;

            // ID pays, nom pays, code, code iso3
            PreparedStatement lpst = icn.prepareStatement("CALL paysSelectAll()");
            ResultSet lrs = lpst.executeQuery();
            while (lrs.next()) {
                tLigne = new Object[4];
                for (int i = 0; i < tLigne.length; i++) {
                    tLigne[i] = lrs.getString(i + 1);
                }

                idtm.addRow(tLigne);
            }
            lrs.close();
            lpst.close();

            //jLabelMessage.setText("Jusque là tout va bien !!!");
        } catch (SQLException ex) {
            jLabelMessage.setText(ex.getMessage());
        }
    } /// remplirJTable

    /**
     *
     */
    private void viderJTable() {
        for (int i = idtm.getRowCount() - 1; i >= 0; i--) {
            idtm.removeRow(i);
        }
    } /// viderJTable


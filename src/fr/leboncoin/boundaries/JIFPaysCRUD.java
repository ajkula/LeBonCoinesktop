    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        try {
            PreparedStatement lpst = icn.prepareStatement("CALL paysInsert(?,?,?,?)");
            lpst.setInt(1, 0);
            lpst.setString(2, jTextFieldNomDuPays.getText());
            lpst.setString(3, jTextFieldCodePays.getText());
            lpst.setString(4, jTextFieldCodeISO3.getText());
            lpst.executeUpdate();
            icn.commit();
            jLabelMessage.setText("Nouveau pays ajouté !");
        } catch (SQLException e) {
            jLabelMessage.setText(e.getMessage());
        }
    }//GEN-LAST:event_jButtonAjouterActionPerformed


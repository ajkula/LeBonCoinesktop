    private void remplirJTableEtJCombo() {
        try {

            idtm = (DefaultTableModel) jTableDepartements.getModel();

            Object[] tLigne;

            PreparedStatement lpst = icn.prepareStatement("CALL departementSelectAll()");
            ResultSet lrs = lpst.executeQuery();

            while (lrs.next()) {
                tLigne = new Object[4];
                for (int i = 0; i < tLigne.length; i++) {
                    // JTable
                    tLigne[0] = lrs.getString(1); // ID DEPT
                    tLigne[1] = lrs.getString(4); // NOM
                    tLigne[2] = lrs.getString(3); // CODE
                    tLigne[3] = lrs.getString(2); // ID REGION

                }

                idtm.addRow(tLigne);
            }

            lrs.close();
            lpst.close();

            /*
             REGION
             */
            // ID REGION, ID PAYS, NOM REGION, CODE REGION
            lpst = icn.prepareStatement("CALL regionSelectAll()");
            lrs = lpst.executeQuery();

            while (lrs.next()) {
                jComboBoxRegions.addItem(lrs.getString(3));

                mapRegionsIdNom.put(lrs.getString(1), lrs.getString(3));
                mapRegionsNomId.put(lrs.getString(3), lrs.getString(1));
            }

            lrs.close();
            lpst.close();

            //jLabelMessage.setText("Jusque là tout va bien !!!");
        } catch (SQLException ex) {
            jLabelMessage.setText(ex.getMessage());
        }
    } /// remplirJTable

    /**
     *
     */
    private void viderJTable() {
        for (int i = idtm.getRowCount() - 1; i >= 0; i--) {
            idtm.removeRow(i);
        }
    } /// viderJTable


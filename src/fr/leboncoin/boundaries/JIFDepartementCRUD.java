    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        try {
            PreparedStatement lpst = icn.prepareStatement("CALL departementInsert(?,?,?,?)");
            lpst.setInt(1, 0);
            lpst.setString(2, jTextFieldIdRegion.getText());
            lpst.setString(3, jTextFieldCodeDepartement.getText());
            lpst.setString(4, jTextFieldNomDuDepartement.getText());

            lpst.executeUpdate();
            icn.commit();
            jLabelMessage.setText("Nouveau département ajouté !");
        } catch (SQLException e) {
            jLabelMessage.setText(e.getMessage());
        }
    }//GEN-LAST:event_jButtonAjouterActionPerformed

